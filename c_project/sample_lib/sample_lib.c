#include "sample_lib.h"


/**********************************************************************
 * NAME: sample_add
 * DISCRIPTION: Calculate the sum of two numbers.
 * ARGUMENTS:
 *   int a: Integer
 *   int b: Integer
 * RETURNS:
 *   int : Calculation result (a + b)
**********************************************************************/
int sample_add(int a, int b) {
    return a + b;
}


/**********************************************************************
 * NAME: sample_sub
 * DISCRIPTION: Calculate the difference between two numbers.
 * ARGUMENTS:
 *   int a: Integer
 *   int b: Integer
 * RETURNS:
 *   int : Calculation result (a - b)
**********************************************************************/
int sample_sub(int a, int b) {
    return a - b;
}


/**********************************************************************
 * NAME: sample_mul
 * DISCRIPTION: Calculate the product of two numbers.
 * ARGUMENTS:
 *   int a: Integer
 *   int b: Integer
 * RETURNS:
 *   int : Calculation result (a * b)
**********************************************************************/
int sample_mul(int a, int b) {
    return a * b;
}


/**********************************************************************
 * NAME: sample_div
 * DISCRIPTION: Calculate division of two numbers.
 * ARGUMENTS:
 *   int a: Integer
 *   int b: Integer
 * RETURNS:
 *   float : Calculation result (a / b)
**********************************************************************/
float sample_div(int a, int b) {
    return (float)a / b;
}
